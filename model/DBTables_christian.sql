	SALARY VARCHAR(50),
	BONUS_PERC_YEAR VARCHAR(50),
	MARKET_RATE_YEAR VARCHAR(50),
	INCOME_TAX_RATE_YEAR VARCHAR(50),
	DONATIONS_PERC_YEAR VARCHAR(50),

