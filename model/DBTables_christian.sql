	SAVE_ABOVE_CASH_ON_HAND VARCHAR(50),

	-- EXTRAS
	CUSTOM1 VARCHAR(50),
	CUSTOM2 VARCHAR(50),
	CUSTOM3 VARCHAR(50),
	CUSTOM4 VARCHAR(50),
	CUSTOM5 VARCHAR(50),
	CUSTOM6 VARCHAR(50),
	CUSTOM7 VARCHAR(50),
	CUSTOM8 VARCHAR(50),
	CUSTOM9 VARCHAR(50),
	CUSTOM0 VARCHAR(50),

	PRIMARY KEY (PL_ID, U_ID)
);

